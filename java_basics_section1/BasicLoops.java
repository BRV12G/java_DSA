package java_basics_section1;

public class BasicLoops {
    
    public static void main(String[] args) {
        //if 
        int a = 20;
        if (a==20){
            System.out.println("hello " + a);
        }

        //while 
        int count = 1;
        while(count!=5){
            System.out.println(count);
            count++;
        }

        //for 
        for(int count2 = 1 ; count2 != 5 ; count2++){
            System.out.println(count2);

        }
        
    }
    
}
